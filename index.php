                <p>Java este un limbaj de programare orientat-obiect, puternic tipizat, conceput de către James Gosling
                    la Sun Microsystems (acum filială Oracle) la începutul anilor ʼ90, fiind lansat în 1995. Cele mai
                    multe aplicații distribuite sunt scrise în Java, iar noile evoluții tehnologice permit utilizarea sa
                    și pe dispozitive mobile gen telefon, agenda electronică, palmtop etc. În felul acesta se creează o
                    platformă unică, la nivelul programatorului, deasupra unui mediu eterogen extrem de diversificat.
                    Acesta este utilizat în prezent cu succes și pentru programarea aplicațiilor destinate
                    intranet-urilor.[10]

                    Limbajul împrumută o mare parte din sintaxă de la C și C++, dar are un model al obiectelor mai
                    simplu și prezintă mai puține facilități de nivel jos. Un program Java compilat, corect scris, poate
                    fi rulat fără modificări pe orice platformă care e instalată o mașină virtuală Java (engleză Java
                    Virtual Machine, prescurtat JVM). Acest nivel de portabilitate (inexistent pentru limbaje mai vechi
                    cum ar fi C) este posibil deoarece sursele Java sunt compilate într-un format standard numit cod de
                    octeți (engleză byte-code) care este intermediar între codul mașină (dependent de tipul
                    calculatorului) și codul sursă.

                    Mașina virtuală Java este mediul în care se execută programele Java. În prezent, există mai mulți
                    furnizori de JVM, printre care Oracle, IBM, Bea, FSF. În 2006, Sun a anunțat că face disponibilă
                    varianta sa de JVM ca open-source.

                    Există 4 platforme Java furnizate de Oracle:

                    Java Card - pentru smartcard-uri (carduri cu cip);
                    Java Platform, Micro Edition (Java ME) — pentru hardware cu resurse limitate, gen PDA sau telefoane
                    mobile;
                    Java Platform, Standard Edition (Java SE) — pentru sisteme gen workstation, este ceea ce se găsește
                    pe PC-uri;
                    Java Platform, Enterprise Edition (Java EE) — pentru sisteme de calcul mari, eventual
                    distribuite.</p>
